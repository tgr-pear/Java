package ex100;

public class ex03 {

	public static void main(String[] args) {
		// 콘솔에 println 또는 print문을 이용하여 다음 문장을 출력하시오.
		 /* 입력 예시 : 없음 / 
			출력 예시 : Hello 
		  				World */
		/* 도움말 : \n은 줄바꿈 특수문자 
		 			문자열은 " / 문자 한 자는 ' 사용 */
		
		System.out.println("Hello" + '\n' + "World");
		/* System.out.println( "Hello" );
	  	   System.out.println( "World" ); */
		
	}

}
